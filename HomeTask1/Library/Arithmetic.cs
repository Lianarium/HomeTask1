using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Library
{
	public class Arithmetic
	{
		Arithmetic()
		{ }

		public static int sumTwoNumbers(int num1, int num2)
		{
			return (num1 + num2);
		}

		public static int subTwoNumbers(int num1, int num2)
		{
			return (num1 - num2);
		}

		public static int multTwoNumbers(int num1, int num2)
		{
			return (num1 * num2);
		}

		public static int divTwoNumbers(int num1, int num2)
		{
			return (num1 / num2);
		}

	}
}
